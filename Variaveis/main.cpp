#include <iostream>

int main() {
    int inteiro = 0;
    float decimal = 5.5f;
    double maiorDecimal = 10.1234;
    char letra = 'A';
    bool verdadeiro = true;
    std::string texto = "Ola";

    std::cout << "Inteiro: " << inteiro << std::endl;
    std::cout << "Float : " << decimal << std::endl;
    std::cout << "Double :" << maiorDecimal << std::endl;
    std::cout << "Char: " << letra << std::endl;
    std::cout << "Booleano: " << verdadeiro << std::endl;
    std::cout << "String: " << texto << std::endl;

    return 0;
}